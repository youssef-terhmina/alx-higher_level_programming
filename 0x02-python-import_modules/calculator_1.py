#!/usr/bin/python3

def add(x, y):
    """My addition function
    Args:
        x: first integer
        y: second integer
    Returns:
        The return value. x + y
    """
    return (x + y)


def sub(x, y):
    """My subtraction function
    Args:
        x: first integer
        y: second integer
    Returns:
        The return value. x - y
    """
    return (x - y)


def mul(x, y):
    """My multiplication function
    Args:
        x: first integer
        y: second integer
    Returns:
        The return value. x * y
    """
    return (x * y)


def div(x, y):
    """My division function
    Args:
        x: first integer
        y: second integer
    Returns:
        The return value. x / y
    """
    return int(x / y)
